#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int j;
	if (argc == 1)
	{
		write (1, "\n", 1);
		return (0);
	}
	i = 1;
	j = 0;
	while (argv[i]){
		i++;
	}
	while (argv[i - 1][j] != '\0'){
		j++;
	}
	write (1, &*argv[i-1], j);
	write (1, "\n", 1);
	return (0);
}
